package com.testing.sample;

public class SampleUtil {

	public static Integer add(Integer a, Integer b) {
		if (a == null) {
			return null;
		}
		if (b == null) {
			return null;
		}
		return a + b;
	}
	public static Integer minus(Integer a, Integer b) {
		if (a == null) {
			return null;
		}
		if (b == null) {
			return null;
		}
		return a - b;
	}
}
